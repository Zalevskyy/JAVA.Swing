    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed

        String nameSelectedBook = null;
        nameSelectedBook = jListBooks.getSelectedValue().toString();
        ViewControler.deleteBook(nameSelectedBook);
        jListBooks.removeAll();
        jListBooks = ViewControler.getList();
        jScrollPane1.setViewportView(jListBooks);
    }//GEN-LAST:event_jButtonDeleteActionPerformed

