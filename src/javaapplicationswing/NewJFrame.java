        JOptionPane.showMessageDialog(this, "book "+ nameSelectedBook);
        String nameSage = ViewControler.getSageName(nameSelectedBook);
        String description = ViewControler.getBookDescription(nameSelectedBook);
        jTextSages.setText(nameSage);
        jTextDescriptions.setText(description);
