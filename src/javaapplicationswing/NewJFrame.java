    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        if (jTextBookName!=null&&jTextBookDescription!=null&&jTextSageName!=null&&jTextSageCity!=null&&jTextSageAge!=null)
        {
            ViewControler.setNewSageBook(jTextBookName.getText(), jTextBookDescription.getText(), jTextSageName.getText(), jTextSageCity.getText(), jTextSageAge.getText());
            jListBooks.removeAll();
            jListBooks = ViewControler.getList();
            jScrollPane1.setViewportView(jListBooks);
            
            //jTextBookName.removeAll();
            jTextBookName.setText("");
            jTextBookDescription.setText("");//removeAll();
            jTextSageName.setText("");//removeAll();
            jTextSageCity.setText("");//removeAll();
            jTextSageAge.setText("");//removeAll();
            
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

